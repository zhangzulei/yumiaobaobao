package com.yumiaobaobao.app.user.entity;

import java.time.LocalDateTime;

public class v_note {
    private String noteuserinfo;

    private String  noteuserId;

    private int userid;

    private int Noteid;

    private LocalDateTime Collectiontime;
    private String image;
    private String content;
    private String avatarimg;

    public String getNoteuserinfo() {
        return noteuserinfo;
    }

    public void setNoteuserinfo(String noteuserinfo) {
        this.noteuserinfo = noteuserinfo;
    }

    public String getNoteuserId() {
        return noteuserId;
    }

    public void setNoteuserId(String noteuserId) {
        this.noteuserId = noteuserId;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getNoteid() {
        return Noteid;
    }

    public void setNoteid(int noteid) {
        Noteid = noteid;
    }

    public LocalDateTime getCollectiontime() {
        return Collectiontime;
    }

    public void setCollectiontime(LocalDateTime collectiontime) {
        Collectiontime = collectiontime;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getAvatarimg() {
        return avatarimg;
    }

    public void setAvatarimg(String avatarimg) {
        this.avatarimg = avatarimg;
    }
}
